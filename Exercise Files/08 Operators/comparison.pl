use strict;
use warnings;

main(@ARGV);

sub main
{
	#when comparing string use 'eq', 'ne', 'lt', 'gt'
    if("six" eq "six"){
    	message("this is true");
    }
    else{
    	message("Not True.");
    }
}

sub message
{
    my $m = shift or return;
    print("$m\n");
}

sub error
{
    my $e = shift || 'unkown error';
    print("$0: $e\n");
    exit 0;
}

